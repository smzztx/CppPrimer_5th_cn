uint16_t CRC16_test2(uint8_t usDataLen, uint8_t *puchMsg)
{
	unsigned char uchCRCHi = 0xFF;
	unsigned char uchCRCLo = 0xFF;
	unsigned uIndex;

	while (usDataLen--)
	{
		uIndex = uchCRCHi ^ *puchMsg++;

		// std::cout << "alive" << uIndex << std::endl;

		uchCRCHi = uchCRCLo ^ auchCRCHi[uIndex];
		uchCRCLo = auchCRCLo[uIndex];
	}
	return (uchCRCHi << 8 | uchCRCLo);
}

unsigned short CRC16_test3(unsigned char *puchMsg, unsigned short usDataLen)
{
	int i, j;
	unsigned short usRegCRC = 0xFFFF;

	for (i = 0; i < usDataLen; ++i)
	{
		usRegCRC ^= *puchMsg++;
		for (j = 0; j < 8; ++j)
		{
			if (usRegCRC & 0x0001)
			{
				usRegCRC = usRegCRC >> 1 ^ 0xA001;
			}else
			{
				usRegCRC >>= 1;
			}
		}
	}
	return usRegCRC;
}

